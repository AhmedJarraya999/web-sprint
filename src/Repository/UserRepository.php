<?php

namespace App\Repository;

use App\Data\SearchData;
use App\Entity\User;
use Doctrine\Bundle\DoctrineBundle\Repository\ServiceEntityRepository;
use Doctrine\ORM\OptimisticLockException;
use Doctrine\ORM\ORMException;
use Doctrine\Persistence\ManagerRegistry;
use Symfony\Component\Security\Core\Exception\UnsupportedUserException;
use Symfony\Component\Security\Core\User\PasswordUpgraderInterface;
use Symfony\Component\Security\Core\User\UserInterface;


/**
 * @method User|null find($id, $lockMode = null, $lockVersion = null)
 * @method User|null findOneBy(array $criteria, array $orderBy = null)
 * @method User[]    findAll()
 * @method User[]    findBy(array $criteria, array $orderBy = null, $limit = null, $offset = null)
 */
class UserRepository extends ServiceEntityRepository implements PasswordUpgraderInterface
{
    public function __construct(ManagerRegistry $registry)
    {
        parent::__construct($registry, User::class);
    }

    /**
     * @throws ORMException
     * @throws OptimisticLockException
     */
    public function add(User $entity, bool $flush = true): void
    {
        $this->_em->persist($entity);
        if ($flush) {
            $this->_em->flush();
        }
    }

    /**
     * @throws ORMException
     * @throws OptimisticLockException
     */
    public function remove(User $entity, bool $flush = true): void
    {
        $this->_em->remove($entity);
        if ($flush) {
            $this->_em->flush();
        }
    }

    /**
     * Used to upgrade (rehash) the user's password automatically over time.
     */
    public function upgradePassword(UserInterface $user, string $newHashedPassword): void
    {
        if (!$user instanceof User) {
            throw new UnsupportedUserException(sprintf('Instances of "%s" are not supported.', \get_class($user)));
        }

        $user->setPassword($newHashedPassword);
        $this->_em->persist($user);
        $this->_em->flush();
    }
    /** 
     *@return User[]
     */
    public function findSearch(SearchData $search): array
    {
        $query = $this->createQueryBuilder('u')->select('u');



        if ($search->q || $search->p) {
            $query =
                $query
                ->where('u.firstname LIKE :q')
                ->setParameter('q', '%' . $search->q . '%')
                ->andWhere('u.lastname LIKE :p ')
                ->setParameter('p', '%' . $search->p . '%');
        }




        return $query->getQuery()->getResult();
    }

    public function findByFullName(string $search): array
    {
        $query = $this->createQueryBuilder('u')->select('u');

        if ($search !== '') {
            $query =
                $query
                ->where(
                    $query->expr()->like(
                        $query->expr()->concat(
                            $query->expr()->concat('u.firstname', $query->expr()->literal(' ')),
                            'u.lastname'
                        ),
                        ':keyword'
                    )    
                )->setParameter('keyword', '%'. $search .'%');
        }

        return $query->getQuery()->getResult();
    }



